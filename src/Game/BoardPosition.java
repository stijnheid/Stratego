package Game;

/**
 *
 */
public class BoardPosition {
    
    private final int x;
    private final int y;
    
    public BoardPosition(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }
}
